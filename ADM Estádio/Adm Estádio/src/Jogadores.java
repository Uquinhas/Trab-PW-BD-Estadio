public class Jogadores {

	private int codigo_jog;
	private String nome_jog;
	private int num_camisa;
	private String nome_team;
	
	public int getCodigo_jog() {
		return codigo_jog;
	}
	public void setCodigo_jog(int codigo_jog) {
		this.codigo_jog = codigo_jog;
	}
	public String getNome_jog() {
		return nome_jog;
	}
	public int getNum_camisa() {
		return num_camisa;
	}
	public void setNum_camisa(int num_camisa) {
		this.num_camisa = num_camisa;
	}
	public String getNome_team() {
		return nome_team;
	}
	public void setNome_team(String nome_team) {
		this.nome_team = nome_team;
	}
	
}
